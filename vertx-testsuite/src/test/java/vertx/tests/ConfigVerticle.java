import org.vertx.java.testframework.TestUtils;

/**
 * @author <a href="http://tfox.org">Tim Fox</a>
 */
public class ConfigVerticle extends Verticle {

  private TestUtils tu;

  @Override
  public void start() {
    tu = new TestUtils(vertx);
    String val = container.config().getString("animals");
    tu.azzert("armadillos".equals(val));
    tu.testComplete();
  }

  @Override
  public void stop() {
  }
}
